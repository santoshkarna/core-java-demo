package day4.basic;

public class OperatorsDemo4 {

    public static void main(String[] args) {
        //4.Relational Operator > <  >=  <=  ==  !=
        int a = 10;
        int b = 20;
        System.out.println(a > b);//false
        System.out.println(a < b);//true
        System.out.println(a >= b);//false
        System.out.println(a <= b);//true
        System.out.println(a == b);//false
        System.out.println(a != b);//true
    }
}
